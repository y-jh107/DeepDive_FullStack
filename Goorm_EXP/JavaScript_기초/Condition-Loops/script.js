const numArray = [2, 7, 13, 29, 13, 16, 8, 36, 19];

console.log(numArray);

for (let i = 0; i < numArray.length; i++) {
    if (numArray[i] % 2 === 0) {
        console.log(`${i}번째 숫자는 짝수입니다.(${numArray[i]})`);
    }
}

const filterArray = [3, 25, 19, 12, 48, 92, 10];

for (number of filterArray) {
    if (number % 3 === 0) {
        console.log(`${number}는 3의 배수입니다.`);
    }
}